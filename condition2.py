# Make a list and ask Python for the answers: True/Flase. 
fruit = ['apple', 'orange', 'grape', 'banana']
if 'apple' in fruit:
   print('apple' in fruit)
   print("Nice guess, 'apple' is in fruit")
# Also can use 'not in' to create a negtive condition.
fruit_1 = 'kiwi'   
if fruit_1 not in fruit:
   print(fruit_1 not in fruit)
   print("Nice, 'kiwi' is not in fruit.")